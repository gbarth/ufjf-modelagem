    private void cadastraCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastraCActionPerformed
        if(textoN.getText().equals("") || textC.getText().equals("") || textE.getText().equals("") || textT.getText().equals("") || textI.getText().equals(""))
          JOptionPane.showMessageDialog(null,"Preencha todos os campos.",null,JOptionPane.ERROR_MESSAGE);  
        else{
            String nome = this.textoN.getText().trim();
            String tel = this.textT.getText().trim();
            String end = this.textE.getText().trim();
            String cpf = this.textC.getText().trim();
            String idade = this.textI.getText().trim();
        

            if(Pessoa.verificaContato(tel)==true){
                DefaultTableModel val = (DefaultTableModel) tbC.getModel();
                val.addRow(new String[]{nome, tel, cpf, end, idade});
                
                textoN.setText("");
                textC.setText("");
                textI.setText("");
                textE.setText("");
                textT.setText(""); 
                 
                textoN.requestFocus();
                 
                Cliente cli = new Cliente (nome, cpf, tel, end, idade);
                cliente.add(cli);
            }else{
                JOptionPane.showMessageDialog(null,"Digite apenas números no telefone.",null,JOptionPane.ERROR_MESSAGE);
                textT.setText("");
            
                textT.requestFocus();
            }
        }
    }//GEN-LAST:event_cadastraCActionPerformed

    private void apagaCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apagaCActionPerformed
        if(tbC.getSelectedRow()== -1)
            JOptionPane.showMessageDialog(null,"Selecione uma linha antes de apagar.",null,JOptionPane.INFORMATION_MESSAGE);
        else{
            if(textoN.getText().equals("") || textT.getText().equals("") || textC.getText().equals("") || textE.getText().equals("") || textI.getText().equals("")){
                int i = tbC.getSelectedRow();
                Cliente.cliente.get(tbC.getSelectedRow()).setNome("");
                ((DefaultTableModel) tbC.getModel()).removeRow(tbC.getSelectedRow());
                cliente.remove(i);
            }
            else
                JOptionPane.showMessageDialog(null,"Atualize a edição antes de apagar.",null,JOptionPane.INFORMATION_MESSAGE);
        }   
    }//GEN-LAST:event_apagaCActionPerformed

