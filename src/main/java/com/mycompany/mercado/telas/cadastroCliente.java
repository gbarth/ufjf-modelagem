    private void editaCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editaCActionPerformed
        int i;
        i = tbC.getSelectedRow();
        
        if(i == -1)
            JOptionPane.showMessageDialog(null,"Selecione uma linha para edição.",null,JOptionPane.INFORMATION_MESSAGE);
        else{
            textoN.setText(cliente.get(i).getNome());
            textC.setText(cliente.get(i).getDocumento());
            textE.setText(cliente.get(i).getEndereco());
            textT.setText(cliente.get(i).getContato());
            textI.setText(cliente.get(i).getIdade());
        }
    }//GEN-LAST:event_editaCActionPerformed

    private void atualizaCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizaCActionPerformed
        
        if(textoN.getText().equals("") || textT.getText().equals("") || textE.getText().equals("") || textC.getText().equals("") || textI.getText().equals(""))
            JOptionPane.showMessageDialog(null,"Selecione uma linha e clique em \"Editar\" antes de atualizar.",null,JOptionPane.INFORMATION_MESSAGE);
        else{
            String nome = this.textoN.getText().trim();
            String tel = this.textT.getText().trim();
            String end = this.textE.getText().trim();
            String cpf = this.textC.getText().trim();
            String idade = this.textI.getText().trim();
            
            int z = tbC.getSelectedRow();
            cliente.remove(z);
            Cliente cli = new Cliente (nome, cpf, tel, end, idade);
            cliente.add(cli);
                       
            ((DefaultTableModel) tbC.getModel()).removeRow(tbC.getSelectedRow());
            DefaultTableModel val = (DefaultTableModel) tbC.getModel();
            val.addRow(new String[]{nome, tel, cpf, end, idade});
            
            
            textoN.setText("");
            textC.setText("");
            textE.setText("");
            textT.setText("");
            textI.setText("");
                 
            textoN.requestFocus();
        }
    }//GEN-LAST:event_atualizaCActionPerformed

