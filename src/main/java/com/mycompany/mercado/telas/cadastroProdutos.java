  private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) { // GEN-FIRST:event_jButton1ActionPerformed
    String nome;
    String marca;
    String validade;
    String codigo_barras;
    String unidade_medida;
    float preco;
    
    try {
        nome = this.jTextField2.getText();
        marca = this.jTextField3.getText();
        validade = this.jTextField4.getText();
        preco = Float.parseFloat(this.jTextField5.getText());
        unidade_medida = buttonGroup.getSelection().getActionCommand();
        codigo_barras = this.jTextField6.getText();
    

        Produto produtoBackup = new Produto(nome, marca, validade, codigo_barras, unidade_medida, preco, 0);

        Estoque.estoque.add(produtoBackup);
        
        JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso!");
    
        this.dispose();
        cadastroProdutos irParaCadastroProdutos = new cadastroProdutos(this);
        irParaCadastroProdutos.setVisible(true);
        irParaCadastroProdutos.setResizable(false);
        this.setEnabled(false);

        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");

    } catch (Exception e) {
        //TODO: handle exception
        JOptionPane.showMessageDialog(null, "ERRO!\nPor favor, verifique as informacoes...");
        System.out.println(e);
    }


 
} // GEN-LAST:event_jButton1ActionPerformed

