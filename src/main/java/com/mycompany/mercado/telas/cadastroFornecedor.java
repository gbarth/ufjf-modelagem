    private void editaForActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editaForActionPerformed
        int i;
        i = tableFor.getSelectedRow();
              
        if(i == -1)
            JOptionPane.showMessageDialog(null,"Selecione uma linha para edição.",null,JOptionPane.INFORMATION_MESSAGE);
        else{
            cnpjFor.setText(fornecedor.get(i).getDocumento());
            nomeFor.setText(fornecedor.get(i).getNome());
            endFor.setText(fornecedor.get(i).getEndereco());
            telFor.setText(fornecedor.get(i).getContato());
        }
    }//GEN-LAST:event_editaForActionPerformed

    private void atualizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizaActionPerformed

        if(nomeFor.getText().equals("") || telFor.getText().equals("") || endFor.getText().equals("") || cnpjFor.getText().equals(""))
            JOptionPane.showMessageDialog(null,"Selecione uma linha e clique em \"Editar\" antes de atualizar.",null,JOptionPane.INFORMATION_MESSAGE);
        else{
            String nome = this.nomeFor.getText().trim();
            String tel = this.telFor.getText().trim();
            String end = this.endFor.getText().trim();
            String cnpj = this.cnpjFor.getText().trim();
            
            int z = tableFor.getSelectedRow();
            fornecedor.remove(z);
            Fornecedor forn = new Fornecedor (nome, cnpj, tel, end);
            fornecedor.add(forn);
            
            ((DefaultTableModel) tableFor.getModel()).removeRow(tableFor.getSelectedRow());
            DefaultTableModel val = (DefaultTableModel) tableFor.getModel();
            val.addRow(new String[]{nome, tel, cnpj, end});            
            
            telFor.setText("");
            cnpjFor.setText("");
            endFor.setText("");
            nomeFor.setText("");
                 
            nomeFor.requestFocus();
        }
        
    }//GEN-LAST:event_atualizaActionPerformed

