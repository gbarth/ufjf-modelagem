    private void cadastraForActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastraForActionPerformed
        if(nomeFor.getText().equals("") || telFor.getText().equals("") || endFor.getText().equals("") || cnpjFor.getText().equals(""))
          JOptionPane.showMessageDialog(null,"Preencha todos os campos.",null,JOptionPane.ERROR_MESSAGE);  
        else{
            String nome = this.nomeFor.getText().trim();
            String tel = this.telFor.getText().trim();
            String end = this.endFor.getText().trim();
            String cnpj = this.cnpjFor.getText().trim();
        
            if(Pessoa.verificaContato(tel)==true){
                 DefaultTableModel val = (DefaultTableModel) tableFor.getModel();
                 val.addRow(new String[]{nome, tel, cnpj, end});
                 telFor.setText("");
                 cnpjFor.setText("");
                 endFor.setText("");
                 nomeFor.setText("");
                 
                 nomeFor.requestFocus();
                 
            Fornecedor forn = new Fornecedor (nome, cnpj, tel, end);
            fornecedor.add(forn);
            }else{
                JOptionPane.showMessageDialog(null,"Digite apenas números no telefone.",null,JOptionPane.ERROR_MESSAGE);
                telFor.setText("");
        
                telFor.requestFocus();
            }
        }
    }//GEN-LAST:event_cadastraForActionPerformed

    
    private void apagaForActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apagaForActionPerformed
        if(tableFor.getSelectedRow()== -1)
            JOptionPane.showMessageDialog(null,"Selecione uma linha antes de apagar.",null,JOptionPane.INFORMATION_MESSAGE);
        else{
            if(nomeFor.getText().equals("") || telFor.getText().equals("") || cnpjFor.getText().equals("") || endFor.getText().equals("")){
                int i = tableFor.getSelectedRow();
                Fornecedor.fornecedor.get(tableFor.getSelectedRow()).setNome("");
                ((DefaultTableModel) tableFor.getModel()).removeRow(tableFor.getSelectedRow());
                fornecedor.remove(i);
            }
            else
                JOptionPane.showMessageDialog(null,"Atualize a edição antes de apagar.",null,JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_apagaForActionPerformed

