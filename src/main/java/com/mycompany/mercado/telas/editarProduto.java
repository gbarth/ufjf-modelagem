    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String nome;
        String marca;
        String validade;
        String codigo_barras;
        String unidade_medida;
        float preco;
        
       try {
           nome = this.jTextField2.getText();
           marca = this.jTextField3.getText();
           validade = this.jTextField4.getText();
           preco = Float.parseFloat(this.jTextField5.getText());
           unidade_medida = buttonGroup.getSelection().getActionCommand();
           codigo_barras = this.jTextField6.getText();
   
           Estoque.estoque.get(this.editarId).setCodigo_barras(codigo_barras);
           Estoque.estoque.get(this.editarId).setMarca(marca);
           Estoque.estoque.get(this.editarId).setNome(nome);
           Estoque.estoque.get(this.editarId).setPreco(preco);
           Estoque.estoque.get(this.editarId).setUnidade_medida(unidade_medida);
           Estoque.estoque.get(this.editarId).setValidade(validade);
           
           
           JOptionPane.showMessageDialog(null, "Produto editado e atualizado com sucesso!");
 
           this.dispose();
           telaEstoque irParaTelaEstoque = new telaEstoque(this);
           irParaTelaEstoque.setVisible(true);
           irParaTelaEstoque.setResizable(false);
           this.setEnabled(false);

       } catch (Exception e) {
           //TODO: handle exception
           JOptionPane.showMessageDialog(null, "ERRO!\nPor favor, verifique as informacoes...");
           System.out.println(e);
       }


    }//GEN-LAST:event_jButton1ActionPerformed

