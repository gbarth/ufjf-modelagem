    private void cFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cFuncionarioActionPerformed

        String nome = this.nomeFuncionario.getText().trim();
        String id = this.idFuncionario.getText().trim();
        String cpf = this.cpfFuncionario.getText().trim();
        String end = this.endFuncionario.getText().trim();
        String tel = this.telFuncionario.getText().trim();
        String cargo = (String) cargoFuncionario.getSelectedItem();
        
        if(Pessoa.verificaContato(tel)==true){
        DefaultTableModel val = (DefaultTableModel) tableFunc.getModel();
        val.addRow(new String[]{nome, cpf, id, end, tel, cargo});
        
        nomeFuncionario.setText("");
        idFuncionario.setText("");
        cpfFuncionario.setText("");
        telFuncionario.setText("");
        endFuncionario.setText("");
        
        nomeFuncionario.requestFocus();
        
        Funcionario funcionario = new Funcionario (id, cargo, nome, cpf, tel, end);
        func.add(funcionario);

        }
        else{
            JOptionPane.showMessageDialog(null,"Digite apenas números no telefone.",null,JOptionPane.ERROR_MESSAGE);
            telFuncionario.setText("");
            telFuncionario.requestFocus();
        }
    }//GEN-LAST:event_cFuncionarioActionPerformed

    private void apagaFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apagaFuncActionPerformed
        if(tableFunc.getSelectedRow() == -1)
            JOptionPane.showMessageDialog(null,"Selecione uma linha antes de apagar.",null,JOptionPane.INFORMATION_MESSAGE);
        else{
            if(nomeFuncionario.getText().equals("") || endFuncionario.getText().equals("") || telFuncionario.getText().equals("") || cpfFuncionario.getText().equals("") || idFuncionario.getText().equals("")){  
            int i = tableFunc.getSelectedRow();
            Funcionario.func.get(tableFunc.getSelectedRow()).setNome("");
            ((DefaultTableModel) tableFunc.getModel()).removeRow(tableFunc.getSelectedRow());
            func.remove(i);}
            else
                JOptionPane.showMessageDialog(null,"Atualize a edição antes de apagar.",null,JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_apagaFuncActionPerformed

