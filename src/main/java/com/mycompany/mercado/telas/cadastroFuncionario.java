    private void editaFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editaFuncActionPerformed
        int i;
        i = tableFunc.getSelectedRow();
        if(i == -1)
            JOptionPane.showMessageDialog(null,"Selecione uma linha para edição.",null,JOptionPane.INFORMATION_MESSAGE);
        else{
            nomeFuncionario.setText(func.get(i).getNome());
            cpfFuncionario.setText(func.get(i).getDocumento());
            endFuncionario.setText(func.get(i).getEndereco());
            telFuncionario.setText(func.get(i).getContato());
            idFuncionario.setText(func.get(i).getId());
            cargoFuncionario.setSelectedItem(func.get(i).getCargo());
        }
    }//GEN-LAST:event_editaFuncActionPerformed

    private void atualizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizaActionPerformed
        if(nomeFuncionario.getText().equals("") || telFuncionario.getText().equals("") || endFuncionario.getText().equals("") || cpfFuncionario.getText().equals("") || idFuncionario.getText().equals(""))
            JOptionPane.showMessageDialog(null,"Selecione uma linha e clique em \"Editar\" antes de atualizar.",null,JOptionPane.INFORMATION_MESSAGE);
        else{
            String nome = this.nomeFuncionario.getText().trim();
            String tel = this.telFuncionario.getText().trim();
            String end = this.endFuncionario.getText().trim();
            String cpf = this.cpfFuncionario.getText().trim();
            String id = this.idFuncionario.getText().trim();
            String cargo = this.cargoFuncionario.getSelectedItem().toString().trim();
            
            int z = tableFunc.getSelectedRow();
            func.remove(z);
            Funcionario funcionario = new Funcionario (id, cargo, nome, cpf, tel, end);
            func.add(funcionario);
            
            ((DefaultTableModel) tableFunc.getModel()).removeRow(tableFunc.getSelectedRow());
            DefaultTableModel val = (DefaultTableModel) tableFunc.getModel();
            val.addRow(new String[]{nome, cpf, id, end, tel, cargo});
            
            telFuncionario.setText("");
            cpfFuncionario.setText("");
            endFuncionario.setText("");
            nomeFuncionario.setText("");
            idFuncionario.setText("");
            cargoFuncionario.setSelectedIndex(0);
                 
            nomeFuncionario.requestFocus();
        }
    }//GEN-LAST:event_atualizaActionPerformed

